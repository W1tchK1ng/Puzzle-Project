FICHA::FICHA()
{
	color		= IDX_COLOR_ROJO;
	forma		= IDX_FORMA_BOX;
	numero		= IDX_NUMERO_1;
	estado		= true;
	x			= 0;
	y			= 0;
}
//---------------------------------------------------------------------------------------------------------------------------
//
//---------------------------------------------------------------------------------------------------------------------------
FICHA::~FICHA()
{


}
//---------------------------------------------------------------------------------------------------------------------------
// set
//---------------------------------------------------------------------------------------------------------------------------
void FICHA::set(int _n,int _f,int _c,int _x,int _y,int _e)
{
	if(_n >= 0)	numero		= _n;
	if(_c >= 0)	color		= _c;
	if(_f >= 0)	forma		= _f;
	if(_x >= 0)	x			= _x;
	if(_y >= 0)	y			= _y;
	if(_e >= 0) estado		= _e;
}
