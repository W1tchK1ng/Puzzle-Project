//---------------------------------------------------------------------------------------------------------------------------
FICHA::FICHA()
{
	clear();
}
//---------------------------------------------------------------------------------------------------------------------------
//
//---------------------------------------------------------------------------------------------------------------------------
FICHA::~FICHA()
{

}
//---------------------------------------------------------------------------------------------------------------------------
// clear
//---------------------------------------------------------------------------------------------------------------------------
void FICHA::clear()
{
	color				= OFF;
	forma				= OFF;
	numero				= OFF;
	flagBreak			= false;
	flagMark			= false;
	flagActive			= false;
	flagActionFall		= false;
	flagActionEscape	= false;
}
//---------------------------------------------------------------------------------------------------------------------------
// set
//---------------------------------------------------------------------------------------------------------------------------
void FICHA::set(int _n,int _f,int _c)
{
	if(_n >= 0)	numero		= _n;
	if(_c >= 0)	color		= _c;
	if(_f >= 0)	forma		= _f;
	flagActive				= true;
}
